# arithmetic operators
x=34
y=6

print(f"additional operator {x+y}")
print(f"substriction operator {x-y}")
print(f"multiplication operator {x*y}")
print(f"division operator {x/y}")
print(f"remainder operator {x%y}")

# comperison operator
print(f"{x} is not equal to {y}",x !=y)
print(f"{x} is not equal to {y}",x ==y)
print(f"{x} is greater than {y}",x >y)
print(f"{x} is less than {y}",x <y)
print(f"{x} is greater than or equal to {y}",x >=y)
print(f"{x} is less than or equal to {y}",x <=y)